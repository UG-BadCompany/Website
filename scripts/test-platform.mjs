import assert from 'node:assert/strict';
import { handler as status } from '../netlify/functions/install-status.mjs';
import { handler as health } from '../netlify/functions/install-health.mjs';
import { handler as draftHandler } from '../netlify/functions/install-draft.mjs';
import { handler as finish } from '../netlify/functions/install-finish.mjs';
import { promises as fs } from 'node:fs';
await fs.rm('.platform-data', { recursive: true, force: true });
const parse = (r) => JSON.parse(r.body);
assert.equal(parse(await status({ httpMethod: 'GET' })).needsInstall, true);
assert.equal(parse(await health({ httpMethod: 'GET' })).ok, true);
assert.equal(parse(await draftHandler({ httpMethod: 'GET' })).draft.currentStep, 'welcome');
let failed = parse(await finish({ httpMethod: 'POST', body: JSON.stringify({ draft: {} }) }));
assert.equal(failed.ok, false);
assert.deepEqual(failed.missing.includes('owner_account'), true);
const completeDraft = { currentStep:'finish', company:{name:'Example Contractor'}, theme:{mode:'system'}, owner:{name:'Owner',email:'owner@example.com'}, services:['HVAC'], modules:['work-orders','invoices'], homepage:{headline:'Reliable contractor services'} };
const saved = parse(await draftHandler({ httpMethod: 'POST', body: JSON.stringify({ draft: completeDraft }) }));
assert.equal(saved.ok, true);
const done = parse(await finish({ httpMethod: 'POST', body: JSON.stringify({ draft: completeDraft }) }));
assert.equal(done.ok, true);
assert.equal(done.redirectTo, '/dashboard/');
assert.equal(parse(await status({ httpMethod: 'GET' })).installationComplete, true);
console.log('Tests passed');
