import assert from 'node:assert/strict';
import { handler as integrationStatus } from '../netlify/functions/integration-status.mjs';
import { handler as finishInstall } from '../netlify/functions/install-finish.mjs';
import { coreModules, defaultPermissions, defaultRoles } from '../netlify/functions/shared/seed-data.mjs';
import { readFile } from 'node:fs/promises';
const integrationResponse=JSON.parse((await integrationStatus({ httpMethod: 'GET' })).body);
const keys=integrationResponse.integrations.map((item)=>item.key);
for (const key of ['MAGIC_LINK_FROM_EMAIL','OPENAI_API_KEY','QUOTE_FROM_EMAIL','RECAPTCHA_SECRET_KEY','RECAPTCHA_SITE_KEY','RESEND_API_KEY','SERPAPI_API_KEY','SITE_URL','SITE_URL_ALIASES','SQUARE_ACCESS_TOKEN','SQUARE_API_VERSION','SQUARE_ENVIRONMENT','SQUARE_LOCATION_ID','SQUARE_WEBHOOK_SIGNATURE_KEY']) assert(keys.includes(key), `${key} missing`);
const source = await Promise.all(['netlify/functions/shared/env-metadata.mjs','public/assets/installer.js','public/assets/platform.js'].map((f)=>readFile(f,'utf8')));
const obsoleteSerpApiName = ['SERPAPI', 'KEY'].join('_');
assert(!source.join('\n').includes(obsoleteSerpApiName), 'obsolete SerpAPI variable name found');
const finish=JSON.parse((await finishInstall({ httpMethod:'POST', body: JSON.stringify({ company:{name:'Test Co'}, owner:{full_name:'Test Owner', email:'Owner@Example.com'}, theme:{mode:'dark', primary:'#123456'} }) })).body);
assert.equal(finish.ok, true);
assert.equal(finish.installation_complete, true);
for (const role of defaultRoles) assert(finish.counts.roles >= defaultRoles.length, `${role} role missing`);
assert(finish.counts.permissions >= defaultPermissions.length);
assert(finish.counts.module_registry >= coreModules.length);
console.log('Platform tests passed.');
