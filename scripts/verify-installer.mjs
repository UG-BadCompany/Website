import fs from 'fs';
import assert from 'node:assert/strict';
const app = fs.readFileSync('src/assets/app.js','utf8');
const finish = fs.readFileSync('netlify/functions/install-finish.js','utf8');
assert.match(app,/if\(!installed\(\) && !path\.startsWith\('\/install'\)\) return navigate\('\/install\/'\)/, 'fresh routes redirect to installer');
assert.match(app,/Installation Recovery/, 'recovery installer exists in app');
assert.match(app,/Missing integrations never block installation/, 'simplified installer warning text exists');
assert.match(finish,/installation_complete:true/, 'finish endpoint sets installation_complete true');
assert.match(finish,/optionalWarnings\(\)/, 'optional integrations warnings only');
assert.ok(fs.existsSync('out/install/index.html') || fs.existsSync('src/assets/app.js'));
console.log('verify:installer passed.');
