import { promises as fs } from 'node:fs';
import path from 'node:path';
const name = process.argv[2];
if (!name) throw new Error('Usage: node scripts/create-migration.mjs descriptive_name');
const dir = path.join(process.cwd(), 'netlify/database');
await fs.mkdir(dir, { recursive: true });
const files = (await fs.readdir(dir)).filter((f) => /^\d+.*\.sql$/.test(f));
const max = files.reduce((n, f) => Math.max(n, Number(f.match(/^(\d+)/)?.[1] || 0)), 0);
const next = String(max + 1).padStart(4, '0');
const safe = name.toLowerCase().replace(/[^a-z0-9]+/g, '_').replace(/^_|_$/g, '');
const file = path.join(dir, `${next}_${safe}.sql`);
await fs.writeFile(file, `-- ${safe}\n`);
console.log(file);
