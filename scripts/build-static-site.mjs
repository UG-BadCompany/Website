import fs from 'fs';
import path from 'path';
const root = process.cwd();
const out = path.join(root, 'out');
fs.rmSync(out, { recursive: true, force: true });
fs.mkdirSync(path.join(out, 'assets'), { recursive: true });
fs.mkdirSync(path.join(out, 'install', 'recovery'), { recursive: true });
fs.mkdirSync(path.join(out, 'config'), { recursive: true });
for (const file of ['app.js','styles.css']) fs.copyFileSync(path.join(root, 'src', 'assets', file), path.join(out, 'assets', file));
const html = (title, script = '/assets/app.js') => `<!doctype html><html lang="en"><head><meta charset="utf-8"><meta name="viewport" content="width=device-width,initial-scale=1"><meta name="color-scheme" content="light dark"><title>${title}</title><link rel="stylesheet" href="/assets/styles.css"><script defer src="${script}"></script></head><body><noscript><main class="shell"><h1>JavaScript required</h1><p>This installer and platform need JavaScript enabled.</p></main></noscript><div id="app"><main class="shell boot"><h1>${title}</h1><p>Loading safely…</p></main></div></body></html>`;
fs.writeFileSync(path.join(out, 'index.html'), html('Contractor CMMS + AI Quoting Platform'));
fs.writeFileSync(path.join(out, 'install', 'index.html'), html('Install Contractor Platform'));
fs.writeFileSync(path.join(out, 'install', 'recovery', 'index.html'), html('Installation Recovery'));
const bootstrap = JSON.parse(fs.readFileSync(path.join(root, 'src', 'config', 'bootstrap.json'), 'utf8'));
fs.writeFileSync(path.join(out, 'config', 'bootstrap.json'), JSON.stringify(bootstrap, null, 2));
console.log('Built static site into out/.');
